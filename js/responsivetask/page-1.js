function ShowSideBar(){
    var ListVisi=document.getElementsByClassName("Listitems")[0];
    ListVisi.style.height="100%";
    ListVisi.style.width="200px";
}
function CloseSideBar(){
    var ListVisi=document.getElementsByClassName("Listitems")[0];
    ListVisi.style.height="100%";
    ListVisi.style.width="0px";
}